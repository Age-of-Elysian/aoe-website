        possible.
      </p>
      <p class="text-justify">
        The basis of protecting and claiming land is based on chunks. The whole
        world is split into 16x16 sections of blocks, and each one is claimable
        by a town. Each town can claim up to a certain amount of townblocks,
        that scales with the town rank. Town ranks change with your town
        population growing. For example, a town with 2 members is considered a
        Hamlet, while a town with 30 members will be called a Metropolis. The
        more people you have, the more land you can claim!
